/**
  * @brief This function handles USART2 global interrupt.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */

		if(__HAL_UART_GET_FLAG(&huart2, UART_FLAG_IDLE) != RESET)       // 空闲中断标记被置位
    {
//			HAL_UART_Transmit(&huart1,"HI-IRQ\r\n",6,10);
        __HAL_UART_CLEAR_IDLEFLAG(&huart2);                         // 清除中断标记
        HAL_UART_DMAStop(&huart2);                                  // 停止DMA接收
        huart2_rx_struct_t.uart_len = UART_RX_LEN - __HAL_DMA_GET_COUNTER(huart2.hdmarx);  // 总数据量减去未接收到的数据量为已经接收到的数据量
        huart2_rx_struct_t.uart_rx_buf[huart2_rx_struct_t.uart_len] = 0;                    // 添加结束符
        huart2_rx_struct_t.uart_rx_flag |= 0x80;                                       // 标记接收结束
        HAL_UART_Receive_DMA(&huart2, huart2_rx_struct_t.uart_rx_buf, 100);    // 重新启动DMA接收
//			HAL_UART_Transmit(&huart1,hlpuart1_rx_struct_t.uart_rx_buf,20,50);
//			memset(hlpuart1_rx_struct_t.uart_rx_buf,0,1000);
		}
	
  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */

  /* USER CODE END USART2_IRQn 1 */
}

